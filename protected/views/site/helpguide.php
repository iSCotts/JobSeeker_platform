<h2>The Rating System</h2>

<p>In the Job Seekers Corner, Job Seekers may review Job Givers and 
Job Givers may review Job Seekers. This is an important concept in the 
system as other users will look at the ratings and make important decisions based upon them. 
Therefor we strongly encourage rating your experience with another user in the system, 
however we will not make rating another user compulsory. We feel that users should not be 
forced to rate another user in order to get the most honest response.</p>

<p>You will be asked to rate the opposite party with 5 aspects of how the job was done namely:
 Quality, Cost, Expertise, Schedule, Response & Professionalism. You can give each one of these 
aspects a rating out of 5 with 1 being very poor and 5 being very good.<p/>

<p>When rating the opposite party ask yourself the following questions with regards to each aspect.<p/>

<p><strong> Quality:</strong> This rating is for the overall quality of the job. Was the job completed 100% or where corners cut?</p>

<p><strong>Cost:</strong> This rating is for the cost of the project. Was the cost reasonable?</p>

<p><strong>Expertise:</strong> Did the parties involved have experience with the matters at hand or where amateur mistakes made?</p>

<p><strong>Schedule:</strong> Was the project completed on time? Was enough time allowed for the task?</p>

<p><strong>Response:</strong> Did both parties respond to each other in a timely fashion?</p>

<p><strong>Professionalism:</strong> Did both parties behave in a professional manner such being punctual.</p>

<p><strong>Is paid:</strong> This rating is to ensure that payment gets made and no one gets ripped off.</p>

<p>Once you have rated the work done. The statistics will be recorded and visible on the other party's profile. 
From there we will calculate an average based on the statistics.</p>

<p>As we have stated above, this is very important. If other users are interested in applying 
for a job, they can look at the statistics and decide whether that person pays them properly. 
One could say that the rating system is for your own protection and it is in your best 
interest to participate in it.</p>
